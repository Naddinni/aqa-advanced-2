/* Long version */
const calculateArea = (width, height) => {
    return  width * height
}

const areaVersion1 = calculateArea(45, 82)
console.log(`Rectangle area is ${areaVersion1} meters` )



/* Short version */
const getArea  = (width, height) => width * height;

const areaVersion2 = getArea (45, 82)
console.log(`Rectangle area is ${areaVersion2} meters` )

