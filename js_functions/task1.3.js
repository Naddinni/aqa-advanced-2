/* Long version */
const rectangleArea = (width, height) => {
    return  width * height
}

const areaVersion1 = rectangleArea(45, 82)
console.log(`Rectangle area is ${areaVersion1} meters` )



/* Short version */
const rectangleSquare = (width, height) => width * height;

const areaVersion2 = rectangleSquare(45, 82)
console.log(`Rectangle area is ${areaVersion2} meters` )

